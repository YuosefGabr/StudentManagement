    public boolean isEmptyStudent() {
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Student name is missing");
            return false;
        }
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showConfirmDialog(this, "Student date of birth is missing");
            return false;
        }
        if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showConfirmDialog(this, "No student from the future are allowed");
            return false;
        }
        if (jTextField3.getText().isEmpty()) {

            JOptionPane.showConfirmDialog(this, "Student email address is missing");
            return false;
        }
        if (!jTextField3.getText().matches("^.+@.+\\..+$")) {

            JOptionPane.showConfirmDialog(this, "Invalid email addressg");
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Student phone number is missing");
            return false;
        }
        if (jTextField4.getText().length() >= 15) {
            JOptionPane.showConfirmDialog(this, "phone number is too long");
            return false;
        }
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Student father name  is missing");
            return false;
        }
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Student mother name   is missing");
            return false;
        }

        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Address line 1  is missing");
            return false;
        }
        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Address line 2  is missing");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showConfirmDialog(this, "Please add your image");
            return false;
        }

        return true;
    }

